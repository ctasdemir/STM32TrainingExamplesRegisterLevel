int32_t UART_get_command(void)
{
	int32_t blink_speed = 0;
	int ch = 0;
	ch = UART_read_byte();
	if(ch != -1)
	{
		 switch(ch)
		 {
			 case '1':
				 blink_speed = 1;
				 break;
			 case '2':
				 blink_speed = 2;
				 break;
			 case '3':
				 blink_speed = 3;
				 break;
			 default:
				 blink_speed = 1;
				 break;
		 }
		 return blink_speed;
	}

	return 0;
}

